			if (Input::GetKeyState(GLFW_KEY_TAB) == KeyState::HOLD)
				glPolygonMode(GL_FRONT_AND_BACK, GL_LINE);
			else if (Input::GetKeyState(GLFW_KEY_TAB) == KeyState::OFF)
				glPolygonMode(GL_FRONT_AND_BACK, GL_FILL);
			
